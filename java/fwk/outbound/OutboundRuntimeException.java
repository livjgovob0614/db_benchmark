package fwk.outbound;

import nexcore.framework.core.exception.SystemRuntimeException;

/**
 * 아웃바운드 예외 클래스
 */
public class OutboundRuntimeException extends SystemRuntimeException {

	private static final long serialVersionUID = -5743437229933671142L;

	public OutboundRuntimeException(String messageCode) {
		super(messageCode);
	}

	public OutboundRuntimeException(String messageCode, String messageParams[]) {
		super(messageCode, messageParams);
	}

	public OutboundRuntimeException(String messageCode, Throwable throwable) {
		super(messageCode, throwable);
	}

	public OutboundRuntimeException(String messageCode, String messageParams[], Throwable throwable) {
		super(messageCode, messageParams, throwable);
	}

}
