package fwk.deferred.reader;

import nexcore.framework.deferred.processor.DeferredSource;

/**
 * Queue에 등록된 디퍼드 객체 확장
 *
 */
public class DeferredSourceExtension extends DeferredSource {
	
	private String logBzopDt; // 로그 영업일

	public void setLogBzopDt(String logBzopDt) {
		this.logBzopDt = logBzopDt;
	}

	public String getLogBzopDt() {
		return logBzopDt;
	}

	public String toSimpleString() {
		return "logBzopDt=" + logBzopDt + ", " + super.toSimpleString();
	}

}
