package fwk.constants.enums;

/**
 * DMS에서 관리하는 SK주식회사 CC 용 부서코드
 * @author greatjin
 *
 */
public enum DMS_MGMT_DEPT {
	
	 ECO_BUSINESS    ("501805", "4283", "Eco 사업팀")
   , DEVICE_BUSINESS ("501802", "4282", "Device사업팀")
	;
	
	private String ccCode    ;  //erp 용 cc Code
	private String hrDeptCode;  // hr deptcode
	private String deptName  ;  // dept명칭

	/**
	 * Constuctor
	 * @param ccCode
	 * @param hrDeptCode
	 * @param deptName
	 */
	DMS_MGMT_DEPT(String ccCode, String hrDeptCode,String deptName)
	{
		this.ccCode     = ccCode     ;
		this.hrDeptCode = hrDeptCode;
		this.deptName   = deptName  ; 
	}

	/**
	 * getCode
	 * @return
	 */
	public String getCode() {
		return ccCode;
	}
	
	/**
	 * Get Name
	 * @return
	 */
	public String getName() {
		return deptName;
	}
	
	/**
	 * getHRCode
	 * @return
	 */
	public String getHRCode() {
		return hrDeptCode;
	}
	
	


}
